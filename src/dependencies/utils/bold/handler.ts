/**
 * Recurso que resalta un valor con texto en **negrita** en una etiqueta HTML.
 * 
 * @function
 * @name bold
 * @param value Valor a resaltar con la etiqueta HTML.
 * @returns Valor concatenado con formato de texto en **Negrita**.
 */
export const bold = ( value: any ) => `<b class="letter-bold">${ value }</b>`;